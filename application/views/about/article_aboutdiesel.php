<div class="article-text">
<p>Diesel is an innovative international lifestyle company, producing a wide-ranging collection of jeans, clothing and accessories.</p>

<p>Since its creation in 1978, Diesel has experienced extraordinary growth and has evolved from being a leading pioneer in denim into the world of premium casual wear, becoming a true alternative to the established luxury market. growth, Diesel's philosophy has remained the same as the day of its creation: Renzo Rosso had envisaged a brand that would stand for passion, individuality and self-expression.
Diesel thrives on change: it produces no less than 3,000 new products every season and each one derives from a process of enormous creative freedom, ensuring constant innovation.</p>

<p>The collections include: Diesel (recently "rebooted" by the arrival of Nicola Formichetti as its Artistic Director), Diesel Black Gold (the contemporary line designed by Andreas Melbostad) and Diesel Kid.
Diesel is not just apparel and denim: it's a lifestyle, which has been interpreted through licenses (under the creative leadership of Andrea Rosso) with leading companies to develop watches and jewellery (with Fossil), eyewear (with Marcolin), fragrances (with L'Oreal), helmets (with AGV), headphones (with Monster), bikes (with Pinarello), strollers (with Bugaboo) and a complete home collection (with Foscarini, Moroso, Zucchi, Seletti and Scavolini).</p>

<p>Diesel Manifesto:
We decode the world around us, take it apart and unlock what we thought we knew. We see differently and unite with those who see it too.
Draw your own path. March in the streets with us. Especially the ones our streets are on.</p>
